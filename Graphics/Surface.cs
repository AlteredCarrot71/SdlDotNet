            for (int i = 0; i < rectangles.Count; i++)
            {
                rects[i] = Surface.ConvertRectToSdlRect(rectangles[i]);
            }
            Sdl.SDL_UpdateRects(this.Handle, rects.Length, rects);
            GC.KeepAlive(this);
        }

