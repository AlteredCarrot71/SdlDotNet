            BaseSdlResource sourceSurface,
            System.Drawing.Rectangle destinationRectangle,
            System.Drawing.Rectangle sourceRectangle)
        {
            if (sourceSurface == null)
            {
                throw new ArgumentNullException("sourceSurface");
