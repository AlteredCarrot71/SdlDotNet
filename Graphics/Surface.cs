        {
            get
            {
                if (this.disposed)
                {
                    throw (new ObjectDisposedException(this.ToString()));
                }
                GC.KeepAlive(this);
                return (Sdl.SDL_Surface)Marshal.PtrToStructure(this.Handle,
                    typeof(Sdl.SDL_Surface));
            }
        }

